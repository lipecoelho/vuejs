<template>
    <div class="componente">
        <h2>As Informações de Usuário</h2>
        <p>Vários detalhes...</p>
        <p>
            Nome do usuário: {{invert()}}
        </p>
    </div>
</template>

<script>
export default {
    props: {
        nome: {
            type: String, 
            default: function() {
                return "Alô"
            }
        }
    },
    methods: {
        invert() {
            return this.nome.split('').reverse().join('');
        }
    }
}
</script>

<style scoped>
    .componente {
        flex: 1;
        background-color: #ec485f;
        color: #fff;
    }
</style>
